package com.github.carlhmitchell.contactablespicker.listViewHelpers;

public class ListItem {
    private String data;
    private int id;

    public ListItem() {
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}